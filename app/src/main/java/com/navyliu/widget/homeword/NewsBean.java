package com.navyliu.widget.homeword;

public class NewsBean {
    private String title, newspaper, status;

    public String getTitle() {
        return title;
    }

    public NewsBean setTitle(String title) {
        this.title = title;
        return this;
    }

    public String getNewspaper() {
        return newspaper;
    }

    public NewsBean setNewspaper(String newspaper) {
        this.newspaper = newspaper;
        return this;
    }

    public String getStatus() {
        return status;
    }

    public NewsBean setStatus(String status) {
        this.status = status;
        return this;
    }
}
